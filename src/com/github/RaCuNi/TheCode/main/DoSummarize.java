package com.github.RaCuNi.TheCode.main;

public class DoSummarize {
	public int sum(int a){
		int y = a;
		return y; 
	}
	
	public int sum(int a, int b){
		int y = a+b;
		return y;
	}
	
	public int sum(int a, int b, int c){
		int y = a+b+c;
		return y;
	}
	
	public int sum(int a, int b, int c, int d){
		int y = a+b+c+d;
		return y;
	}

}
