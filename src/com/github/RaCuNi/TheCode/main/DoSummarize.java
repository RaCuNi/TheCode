package com.github.RaCuNi.TheCode.main;

public class DoSummarize {
	public long sum(long a, long b){
		long y = a+b;
		return y;
	}
	
	public long sum(long a, long b, long c){
		long y = a+b+c;
		return y;
	}
	
	public long sum(long a, long b, long c, long d){
		long y = a+b+c+d;
		return y;
	}

}
